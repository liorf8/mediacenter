CREATE TABLE Users
(
	user_id int GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	login varchar(255) NOT NULL,
	pw_hash varchar(255) NOT NULL,
	reg_ts timestamp DEFAULT CURRENT_TIMESTAMP NOT NULL,
	
	CONSTRAINT uc_login UNIQUE (login),
	CONSTRAINT cc_login CHECK(CHARACTER_LENGTH(login) > 0),
	CONSTRAINT cc_pw_hash CHECK(CHARACTER_LENGTH(pw_hash) > 0)
);