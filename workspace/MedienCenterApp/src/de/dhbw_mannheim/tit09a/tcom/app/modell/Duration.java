package de.dhbw_mannheim.tit09a.tcom.app.modell;

public class Duration
{
    // --------------------------------------------------------------------------------
    // -- Class Variable(s) -----------------------------------------------------------
    // --------------------------------------------------------------------------------
    public static final int MILLIS_PER_HOUR = 60 * 60 * 1000;
    public static final int MILLIS_PER_MIN = 60 * 1000;
    public static final int MILLIS_PER_SEC = 1000;

    // --------------------------------------------------------------------------------
    // -- Instance Variable(s) --------------------------------------------------------
    // --------------------------------------------------------------------------------
    private long totalMillis;

    // --------------------------------------------------------------------------------
    // -- Constructor(s) --------------------------------------------------------------
    // --------------------------------------------------------------------------------
    public Duration(long totalMillis)
    {
	this.totalMillis = totalMillis;
    }

    // --------------------------------------------------------------------------------
    public Duration(int hours, int mins, int secs, int millis)
    {
	millis += hours * MILLIS_PER_HOUR;
	millis += mins * MILLIS_PER_MIN;
	millis += secs * MILLIS_PER_SEC;
	millis += millis;
    }

    // --------------------------------------------------------------------------------
    // -- Public Method(s) ------------------------------------------------------------
    // --------------------------------------------------------------------------------
    public int getHours()
    {
	return (int) (totalMillis / MILLIS_PER_HOUR);
    }

    // --------------------------------------------------------------------------------
    public int getMins()
    {
	return (int) ((totalMillis % MILLIS_PER_HOUR) / MILLIS_PER_MIN);
    }

    // --------------------------------------------------------------------------------
    public int getSecs()
    {
	return (int) ((totalMillis % MILLIS_PER_MIN) / MILLIS_PER_SEC);
    }

    // --------------------------------------------------------------------------------
    public int getMillis()
    {
	return (int) (totalMillis % MILLIS_PER_SEC);
    }

    // --------------------------------------------------------------------------------
    public long getTotalMillis()
    {
	return totalMillis;
    }

    // --------------------------------------------------------------------------------
    public String toString()
    {
	return String.format("%02d:%02d:%02d,%03d", getHours(), getMins(), getSecs(), getMillis());
    }

    // --------------------------------------------------------------------------------
    // --------------------------------------------------------------------------------
    // --------------------------------------------------------------------------------
}
